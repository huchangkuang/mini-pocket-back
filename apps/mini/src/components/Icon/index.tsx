export type IconName = string;

export type IconProps = {
  name: string;
  size?: number | string;
  color?: string;
  className?: string;
  style?: React.CSSProperties;
};

/**
 * 通用图标组件 - 使用 <Image> + SVG 文件渲染
 * 兼容微信小程序（不依赖 CSS mask 或 iconfont）
 * 
 * @param name - 图标名称 (kebab-case，如 "search", "arrow-up")
 * @param size - 尺寸（数字=px，字符串=原样使用），默认 24
 * @param color - 颜色（hex 如 "#707783"），有对应颜色变体时使用
 */
const Icon: FC<IconProps> = memo(({ name, size = 24, color, className, style }) => {
  let src: string;
  if (color && COLORED_ICON_MAP[name]?.[color]) {
    src = COLORED_ICON_MAP[name][color];
  } else {
    src = BASE_ICON_MAP[name] || "";
  }
  if (!src) {
    console.warn("[Icon] Unknown icon:", name);
    return null;
  }
  const pxSize = typeof size === "number" ? `${size}px` : isNaN(Number(size)) ? size : `${size}px`;
  return (
    <Image
      src={src}
      className={className}
      style={{ width: pxSize, height: pxSize, flexShrink: 0, ...style }}
      mode="aspectFit"
    />
  );
});

Icon.displayName = "Icon";
export default Icon;
